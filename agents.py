async def run_single_agent(client, agent_key, agent_info, user_prompt):
    """단일 에이전트 실행 (비동기)"""
    response = await asyncio.to_thread(
        client.messages.create,
        model="claude-haiku-4-5-20251001",
        max_tokens=2000,
        system=agent_info["system"],
        messages=[{"role": "user", "content": user_prompt}],
    )
    return {
        "key": agent_key,
        "name": agent_info["name"],
        "icon": agent_info["icon"],
        "result": response.content[0].text,
    }


async def run_all_agents(api_key, user_prompt):
    """5개 에이전트 병렬 실행"""
    client = anthropic.Anthropic(api_key=api_key)

    tasks = [
        run_single_agent(client, key, info, user_prompt)
        for key, info in AGENTS.items()
    ]
    results = await asyncio.gather(*tasks)
    return results


def run_chief_analyst(api_key, stock_name, agent_results):
    """수석 분석관: 5개 분석 취합하여 최종 보고서 생성"""
    client = anthropic.Anthropic(api_key=api_key)

    # 5개 분석 결과를 하나의 프롬프트로 합침
    combined = f"## 분석 대상: {stock_name}\n\n"
    for r in agent_results:
        combined += f"### {r['icon']} {r['name']} 분석 결과\n"
        combined += r["result"] + "\n\n---\n\n"

    combined += "\n위 5개 분석관의 결과를 종합하여 최종 보고서를 작성하세요."

    response = client.messages.create(
        model="claude-haiku-4-5-20251001",
        max_tokens=4000,
        system=CHIEF_ANALYST_SYSTEM,
        messages=[{"role": "user", "content": combined}],
    )
    return response.content[0].text
